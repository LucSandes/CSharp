using System;

namespace VariaveisConstantes
{
    class Program
    {
        static void Main(string[] args)
        {
            //declarar:
            //tipo nomeDaVariavel

            byte b;
            short s;
            int i;
            long l;


            //atribuir valor:
            //nome = conteúdo
            b = 10;
            s = 100;
            i = 1000;
            l = 10000;

            //ler
            //nome
            Console.WriteLine(b + s + i + l);

            //declarar + atribuir
            //tipo nome = conteudo

            bool fumante = false;

            Console.WriteLine(fumante);

            

            Console.ReadKey();
        }
    }
}
